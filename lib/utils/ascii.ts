/** Compact version for status bar */
export const ASCII_LOGO_SMALL = "KUT/OS";

/** ASCII horizontal rule */
export function hr(width = 60, char = "‚ĒÄ"): string {
  return char.repeat(width);
}

/** ASCII box */
export function box(content: string, width = 60): string {
  const lines = content.split("\n");
  const top = "‚ĒĆ" + "‚ĒÄ".repeat(width - 2) + "‚Ēź";
  const bottom = "‚ĒĒ" + "‚ĒÄ".repeat(width - 2) + "‚Ēė";
  const middle = lines.map((l) => "‚Ēā " + l.padEnd(width - 4) + " ‚Ēā").join("\n");
  return [top, middle, bottom].join("\n");
}

/** ASCII progress bar */
export function progressBar(value: number, max = 100, width = 20): string {
  const filled = Math.round((value / max) * width);
  const empty = width - filled;
  return "‚Ėą".repeat(filled) + "‚ĖĎ".repeat(empty);
}
